_SIC_MAP: dict = {}
for _ff_id, _ff_name, _ranges in _FF48:
    for _lo, _hi in _ranges:
        for _s in range(_lo, _hi + 1):
            if _s not in _SIC_MAP:
                _SIC_MAP[_s] = (_ff_id, _ff_name)

FF48_NAMES: dict = {ff_id: name for ff_id, name, _ in _FF48}


def assign_ff48(sic) -> tuple:
    """Return (ff48_id, industry_name) for a given SIC code."""
    import pandas as pd
    if pd.isna(sic):
        return (48, 'Other')
    return _SIC_MAP.get(int(sic), (48, 'Other'))
